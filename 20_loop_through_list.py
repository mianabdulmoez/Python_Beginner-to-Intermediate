# Using Loop in List
list = ["apple", "banana", "orange", "mango"]
print(len(list))
# Using FOR loop 
for x in list:
    print(x)
# Loop through Index Number
list = ["apple", "banana", "orange", "mango"] 
for i in range(len(list)):
    print(list[i])# i is making range order of its length
# Using While Loop in List
list = ["apple", "banana", "orange", "mango"]
i = 0
while i < len(list):
    print(list[i])
    i = i +1

    